public class print {

    //print promot type => int
    public static void i(int i_ids) {
        System.out.print(i_ids);
    }

    //print promot type => string
    public static void s(String s_names) {
        System.out.print(s_names);
    }

    //print promot type => float
    public static void f(float f_float) {
        System.out.print(f_float);
    }

    //print promot type => double
    public static void d(double d_double) {
        System.out.print(d_double);
    }

    //print promot type => long
    public static void l(long l_long) {
        System.out.print(l_long);
    }

    //print promot type => char
    public static void c(char c_char) {
        System.out.print(c_char);
    }

    //print promot type => boolean
    public static void b(boolean b_boolean) {
        System.out.print(b_boolean);
    }
}
